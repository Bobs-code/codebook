package main

import (
	"fmt"
)


// Strings are series of bytes which usually represent text characters
func main(){
	// \n is the newline character
 	fmt.Println("Hello, \nGo!")
	// \t is the tab character
	fmt.Println("Hello, \tGo!")
	fmt.Println("Quotes: \"Go Here\"")
	fmt.Println("Backslash: \\")
}


