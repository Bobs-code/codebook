package main


import (
	"fmt"
)


func main(){
	// Rune Literals are written with single quotation marks
	// They are kept as numeric codes, not the characters themselves
	// Escape sequences can be used in a rune literal as well
	fmt.Println('A')
	// Booleans are either one of two values - true or false
	fmt.Println(true)
}

